				<h3 class="bottommargin-xs uppercase">ABOUT THE EUROPEAN TYRE LABELING</h3>

				<p class="bottommargin">The European Tyre labelling came into effect in 2012 and mandated that all tyres sold in Europe must have the mandatory tyre labels. The aim of this was to increase the safety, environmental and economic efficiency of road transport by promoting safe and fuel-efficient tyres with low noise levels. These labels provide the end-user clear and standardized information on three criteria: fuel efficiency, wet grip and external rolling noise of tyres so end-users can make more informed choices when purchasing tyres.</p>
				
				<div class="row clearfix">
