						<h3  class="bottommargin-xs uppercase">1. Rolling Resistance/Fuel Efficiency</h3>

						<p class="bottommargin">Rolling Resistance directly impacts fuel efficiency which in turn impacts the environment(CO2 emissions). There are 7 classes from A, (most efficient), to G (least efficient). The effect may vary among vehicles and driving conditions, but the difference between an A and a G class for a complete set of tyres could reduce fuel consumption by up to 7.5 %* and even more in case of trucks.</p>

						<h3  class="bottommargin-xs uppercase">2. Wet Grip</h3>

						<p class="bottommargin">Wet grip is a criteria that is critical for your safety as it affects how quickly you will be able to stop in wet conditions. There are 7 classes from A (shortest braking distances) to G (longest  braking distances). The effects may vary among vehicles and driving conditions, but in the case of full braking, the difference between an A and a G class for a set of four identical tyres could be up to 30% shorter braking distance (e.g. for a typical passenger car driving at 80 km/h speed this could be up to 18m shorter braking distance)*.</p>
