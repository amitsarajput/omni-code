				<p class="bottommargin">This is the amount of external noise a tyre makes when it rolls along the road surface. The driving noise is quoted as an absolute value in decibel and in addition to that a pictogram displays whether the tyre external rolling noise performance is above future European mandatory limit value (3 black waves = noisy tyre), meets the future limit value or is up to 3 dB quieter (2 black waves = average tyre), or is over 3 dB quieter than the future limit value (1 black wave = quiet tyre).</p>
				

