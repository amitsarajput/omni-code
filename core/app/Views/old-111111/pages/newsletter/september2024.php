                      <h2>NEW REGULATIONS</h2>
                    </div>
                    <a id="tab9-1"></a>
                    <div class="content">
                      <p class="bluelink"><b>EU's New Deforestation Regulation (EUDR)</b></p>
                      <p>The European Union has passed a new Deforestation Regulation (EUDR), requiring companies trading in rubber, cattle, cocoa, coffee, oil palm, soya, and wood—along with products derived from these commodities—to conduct extensive due diligence on their value chains. Companies must demonstrate that their products are deforestation-free and not linked to forest degradation, illegal harvesting, or trade. This regulation aims to curb global deforestation and forest degradation and applies to tyre companies selling their products within the EU market or exporting from the EU.</p>
                      <p>The regulation came into force in June 2023, and companies are required to comply with its obligations by December 30, 2024. Radar Tyres is on track to meet these requirements and aims to complete compliance by October 2024.</p>
                      <p><img src="<?= base_url('assets/img/newsletter/september-2024/eudr.jpg');?>"></p>
                    </div>
                    <a id="tab9-2"></a>
                    <div class="content">
                      <p class="bluelink"><b>New European Regulation R117.04</b></p>
                      <p>The new European Reg 117.04 introduces stringent requirements for tyre companies, focusing on improving wet grip performance, reducing rolling resistance, and passing a worn wet grip test. This worn wet grip test requires tyres to be buffed down to 2 mm and then evaluated for wet grip—one of the most demanding standards in the industry.</p>
                      <p>These regulations came into force in 2023 and mandate that any new tyre launched after July 2024 must comply with these stricter standards. Additionally, all existing tyres currently on the European market must be tested and meet the regulation's requirements by 2026. The aim is to give drivers confidence to use the tyres down to the legal limit of 1.6 mm, enhance safety, improve tyre fuel efficiency, and reduce CO2 emissions.</p>
                      <p>Radar Tyres is fully committed to complying with these regulations. Our products are currently undergoing testing to ensure we are on track to meet these new standards.</p>
                      <p><img src="<?= base_url('assets/img/newsletter/september-2024/european-regulation.jpg');?>"></p>
