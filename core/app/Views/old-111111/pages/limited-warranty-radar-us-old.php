				<h3 class="bottommargin-xs uppercase">5. Tire Care and Maintenance Guide</h3>
				<p class="bottommargin-xs">Tire failure can result in serious damage and/or personal injury. To reduce the risk of tire failure we recommend the following:</p>
				<ul class="list-item bmxx bottommargin">
					<li>Maintain proper inflation pressure and do not use when under-inflated or over-inflated. Pressure should be set at the level recommended by the vehicle manufacturer.</li>
					<li>Wheel alignment and balancing should be checked at regular intervals.</by>
						<li>Do not overload. The maximum load carrying capacity is molded on the sidewall of the tire.</li>
						<li>Do not spin your tires excessively (wheel spin) and avoid driving over curbs, potholes, obstacles and edges of pavement.</li>
						<li>Never drive on smooth (bald) tires. By law, tires must be replaced when 2/32” of tread depth remains, as indicated by the tread wear indicators molded into the tread grooves.</li>
						<li>Check your tires frequently for any scrapes, cuts, foreign objects, separations or bulges. If damage is found, do not attempt to dismount tire yourself. Change damaged tire with the spare and contact your local authorized Radar Tires dealer immediately.</li>
						<li>Do not drive in excess of speed limits allowed by law or the maximum speed justified by driving conditions.</li>
						<li>To achieve proper even wear and gain maximum tread life, tire rotation at regular intervals is required.</li>
						<li>Request that your tire and rim assemblies are hand torqued with a calibrated torque wrench to the vehicle manufacturers specifications.</li>
					</ul>


