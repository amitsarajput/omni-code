				<p>As the EU heads towards its goal of becoming CO2 neutral by 2050 it is trying to cut emissions across various sectors including the transport sector. The new EU tyre labelling regulation aims to help achieve this goal by reducing CO2 levels as it provides the consumers information to identify and purchase more fuel efficient tyres.</p>
				<p>The new EU labelling Regulation (EU) 2020/740 comes into effect from 1<sup>st</sup> May 2021 and replaces the previous Regulation (EC) No 1222/2009, which has been in effect from 2012. This labelling scheme allows consumers to make an informed purchase decision on the various performance paraments of the tyres such as rolling resistance which translates into fuel efficiency, wet grip and noise. Additionally, the new labelling also introduces pictograms that indicate if the tyre is suitable for use in severe snow conditions(3PMSF) and its grip in icy conditions (the later applies to C1 tyres only).</p> 
				<p class="botttommargin">
