						<h3  class="bottommargin-xs uppercase">The New EU Tyre Label</h3>
						<ul class="bottommargin ul-bullet">
							<li>The new regulation covers C1(Car & SUV), C2(Van & Light Trucks) and C3(Bus & Truck) tyres</li>
							<li>A tyre type identification number has been added to the label</li>
							<li>A unique QR code for each article has been added that links the to the EPREL (European Product Registry for Energy Labelling)</li>
							<li>Noise Classes A, B and C replace the previous sound waves</li>
							<li>There are options for including pictograms for severe snow conditions (3PMSF) 3 Peak Mountain Snowflake or Alpine symbol. C1 Tyres suitable for ice conditions(Friction) can also be represented with an ice grip pictogram.</li>
							<li>Rolling Resistance label classes have been reclassified – The earlier E class has now been moved to D class, F & G class have now been grouped under E class.</li>
							<li>Wet Grip labels classes have been reclassified - The earlier E class has now been moved to D class, and the earlier F class has now been made E class.</li>

						</ul>

						<h3  class="bottommargin-xs uppercase">How to read the New EU tyre label</h3>
						<div class="text-center">
							<img class="center" src="<?= base_url('assets/img/eulabel-reading-instruction.jpg') ?>" alt="">
