                <div class="col-sm-12">
                  <p>G.S. Sareen is the founder, President and CEO of Omni United, one of the most dynamic companies in the tyre industry. He founded Omni United in 2003, after having identified the market opportunity available to an agile, efficient, customer-service oriented and design-driven global tyre supplier, with state of-the-art logistics and supply chain management. Under his direction, Omni has rapidly grown and evolved in the area of design, manufacturing and distribution of tyres worldwide, offering a complete range of products and service solutions to its customers in over 80 countries.</p>
                  <p>Mr. Sareen has always believed in Speed, Innovation, Sustainability and Giving back to society as the core of the company’s vision. In 2011 he launched the Mobilising Hope Campaign through which the company supported Breast Cancer Research Foundation (BCRF). In early 2012 he set the strategic goal for Omni United to become the first tyre company in the world to become carbon neutral and this was achieved through the Radar Tyres brand in late 2013. In November 2014 he launched Timberland Tyres through an innovative partnership between Timberland and Omni United that was based on sustainability at its core.</p>
