                  <p>Mr. Sareen is an alumnus of the Daly School and the Science College in Bhopal, Madhya Pradesh, India, and is a decorated and distinguished member of the Indian Army where he served for close to seven years. He was awarded a gallantry award for action in 1993 by the President of India. He was born in India and has been a resident in Singapore since 1994.</p>
                  <p>Throughout his professional career, Mr. Sareen has received numerous entrepreneurship and corporate leadership awards and recognitions, the most recent one being felicitated by the government of Madhya Pradesh, India in 2018 for having done pioneering work in his professional field and for his achievements; being included in the prestigious “Singapore Enterprise 50 Awards” for five consecutive years from 2013-2017, for Singapore-based, privately-held companies who have contributed to economic development in Singapore and abroad; in 2014 he was recognised by Peak Magazine as one of Singapore’s top 30 influential business leaders; the same year Prestige Magazine featured him among the 100 headlining movers and shakers in Singapore; in 2013 he was conferred the “Distinguished Business Award” by Promising SME 500 for his commercial success and contributions to the economy and community at large; and many more.</p>
